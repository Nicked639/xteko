function run() {
  $widget.height = 300;
  $ui.render({
    props: {
      title: "Chars Keyboard",
      id: "keyboard",
      bgcolor: $color("white")
    },
    views: [
      {
        type: "matrix",
        props: {
          columns: 9,
          spacing: 0,
          square: true,
          bgcolor: $color("white"),
          data: item1Data,
          template: {
            props: {},
            views: [
              {
                type: "label",
                props: {
                  id: "label",
                  textColor: $color("gray"),
                  bgcolor: $color("white"),
                  radius: 0,
                  align: $align.center,
                  font: $font(20)
                },
                layout: $layout.fill
              }
            ]
          }
        },
        layout: function(make, view) {
          make.edges.insets($insets(0, 0, 35, 0));
        },
        events: {
          didSelect: function(sender, indexPath, data) {
            $keyboard.insert(data.label.text);
            $clipboard.text = data.label.text;
            $ui.toast("符号已复制", 0.3);
            historyCache(data.label.text);
          }
        }
      },
      {
        type: "menu",
        props: {
          items: items
        },
        layout: function(make, view) {
          make.bottom.right.inset(0);
          make.left.inset(25);
          make.height.equalTo(35);
        },
        events: {
          changed: function(sender) {
            dataPick(sender.index);
          }
        }
      },
      {
        type: "button",
        props: {
          id: "closebtn",
          bgcolor: $color("clear"),
          icon: $icon("225", $color("tint"), $size(18, 18))
        },
        layout: function(make, view) {
          make.bottom.inset(4);
          make.left.inset(3);
        },
        events: {
          tapped(sender) {
            $device.taptic(0);
            $widget.height = 181;
            $("keyboard").remove();
            var dataManager = require("../data-manager");
            dataManager.init();
            var path = $app.env == $env.today ? "../widget" : "../app";
            var module = require(path);
            module.init();
            $("input").text = $clipboard.text;
          }
        }
      }
    ]
  });
}
function dataPick(index) {
  if (index == 0) {
    $("matrix").data = item1Data;
  } else {
    var data = symbols[index];
    $("matrix").data = dataPush(data);
  }
}

function dataPush(data) {
  var dataX = [];
  for (var i in data) {
    dataX.push({
      label: {
        text: data[i]
      }
    });
  }
  return dataX;
}

function historyCache(symbol) {
  var item1 = "";
  for (var i in item1Data) {
    if (i == 35) break;
    item1 = item1 + item1Data[i].label.text;
  }
  var duplicate = item1.replace(symbol, "");
  $console.log(duplicate);
  item1 = symbol + duplicate;
  var history = dataPush(item1);
  $cache.set("histories", {
    history: history
  });
  item1 = $cache.get("histories");
  item1Data = item1.history;
}

module.exports = {
  run: run
};
